<?php
	$a = 1;
	$b = 2;
	$c = 2;

	//Mayor que >
	if ($a > $b) {
		echo '$a es mayor que $b';
	} else {
		echo '$a no es mayor que $b';
	}

	echo '<br>';

	//Menor que <
	if ($a < $b) {
		echo '$a es menor que $b';
	} else {
		echo '$a no es menor que $b';
	}

	echo '<br>';

	//Mayor o igual que >=
	if ($c >= $b) {
		echo '$c es mayor o igual a $b';
	} else {
		echo '$c no es mayor o igual a $b';
	}

	echo '<br>';

	//menor o igual <=
	if ($c <= $b) {
		echo '$c es menor o igual a $b';
	} else {
		echo '$c no es menor o igual a $b';
	}

	echo '<br>';

	//Diferente <> o !=
	if ($c != $b) {
		echo '$c es diferente a $b';
	} else {
		echo '$c no es diferente a $b';
	}

	echo '<br>';

	if ($a <> $b) {
		echo '$a es diferente a $b';
	} else {
		echo '$a no es diferente a $b';
	}	

	echo '<br>';

	//Igual ==
	if ($a == $b) {
		echo '$a es igual a $b';
	} else {
		echo '$a no es igual a $b';
	}	

	echo '<br>';
	if ($c == $b) {
		echo '$c es igual a $b';
	} else {
		echo '$c no es igual a $b';
	}			

?>
